// string methods in javascript

let text = "JavaScript Tutorials For";

console.log(text.charAt(4));    // returns char at postion 4 /index wise/
console.log(text.concat(" Beginner"));    // adding at the end to the text variable
console.log(text.endsWith("t"));     // return true when text is end letter is same
console.log(text.indexOf("v"));   // return the index of v in the text
console.log(text.replace("For", "Is"));  // replace the text /"searchtext","replacetext"/
console.log(text.slice(1,4));   // slice the text variable /"startindex", "endindex"/

