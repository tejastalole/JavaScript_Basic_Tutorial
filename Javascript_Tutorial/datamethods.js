// data methods in javascript


let current = new Date()

console.log(current.getDate());   // returns current date
console.log(current.getFullYear());  // returns current full year
console.log(current.getHours());    // returns current hours
console.log(current.getMinutes());   // returns current minutes
console.log(current.getSeconds());  // returns current seconds
console.log(current.getMonth());   // returns current month















